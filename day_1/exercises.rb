puts "Counting Down"
puts "----------------"

x = 10

until x == 0
  puts x
  x -= 1
end

puts "------DONE------\n\n"

puts "Counting by twos"
puts "----------------"

x = 1

until x == 11
  puts x * 2
  x += 1
end


puts "------DONE------\n\n"

puts "Remainders of 3 up to 10"
puts "----------------"

x = 1

until x == 11
  puts x % 3
  x += 1
end


puts "------DONE------\n\n"


